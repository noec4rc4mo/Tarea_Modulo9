    private void AggActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AggActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        detalles.setCodigo(txtCodigo.getText());
        detalles.setNombre(txtNombre.getText());
        detalles.setprecio(Double.parseDouble(txtPrecio.getText()));
        detalles.setcantidad(Double.parseDouble(txtCantidad.getText()));
        detalles.settotal(NORMAL);
        
        
        // Crear un objeto
        
       Object[] row = {detalles.getCodigo(), detalles.getNombre(), detalles.getprecio(), detalles.getcantidad(), detalles.gettotal()};
       
       //Establecer la fila modelo
       
       model.addRow(row);
    }//GEN-LAST:event_AggActionPerformed

